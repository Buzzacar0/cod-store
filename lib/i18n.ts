export const currencies = {
  pt: { code: "BRL", symbol: "R$", rate: 1 },
  en: { code: "USD", symbol: "$", rate: 0.17 },
  fr: { code: "EUR", symbol: "€", rate: 0.16 },
  de: { code: "EUR", symbol: "€", rate: 0.16 },
}

export function formatPrice(price: number, language: string): string {
  const currency = currencies[language]
  const convertedPrice = price * currency.rate
  return `${currency.symbol}${convertedPrice.toFixed(2)}`
}
